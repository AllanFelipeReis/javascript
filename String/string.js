// Propriedade length retorna o tamanho da string
console.log('Allan'.length)

// Propriedade charAt retorna o char que esta na posicao
console.log('Allan'.charAt(2))

// replace que substitui a palavra passada pela nova palavra
console.log('Allan Reis'.replace('Allan', 'Rafaela'))

// toUpperCase Deixa tudo maiusculo
console.log('Allan Reis'.toUpperCase())

// toLowerCase deixa rudo minusculo
console.log('ALLAN REIS'.toLowerCase())
